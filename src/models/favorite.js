import mongoose from 'mongoose';

const favoriteSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    user: {
      name: {
        type: String,
        required: true,
      },
      email: {
        type: String,
        required: true,
      },
    },
  },
  {
    timestamps: true,
  }
);

const Favorite =
  mongoose.models.Favorite || mongoose.model('Favorite', favoriteSchema);

export default Favorite;
